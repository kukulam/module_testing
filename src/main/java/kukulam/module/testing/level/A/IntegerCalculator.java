package kukulam.module.testing.level.A;

class IntegerCalculator {

    /**
     * Adds numbers.
     * Example: 10 + 5 = 15
     * @return result of: a + b
     */
    int add(int a, int b) {
        return a + b;
    }

    /**
     * Subtracts numbers.
     * Example: 10 - 5 = 5
     * @return result of: a -b
     */
    int subtract(int a, int b) {
        return a - b;
    }

    /**
     * Multiply numbers.
     * Example: 10 * 5 = 50
     * @return result of: a * b
     */
    int multiply(int a, int b) {
        return  a * b;
    }

    /**
     * Square number.
     * Example 10 * 10 = 100
     * @return result of: a * a
     */
    int power(int a) {
        return a * a;
    }
}
